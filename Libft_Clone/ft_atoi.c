#include "libft.h"

int		ft_atoi(const char *s)
{
	int num;
	int sign;

	num = 0;
	sign = +1;
	while (*s == '\r' || *s == '\t' || *s == '\v' ||
			*s == '\f' || *s == '\n' || *s == ' ')
		++s;
	if (*s == '-')
		sign = -1;
	if (*s == '-' || *s == '+')
		++s;
	while (*s >= '0' && *s <= '9')
	{
		num = num * 10 + *s - '0';
		++s;
	}
	return (num * sign);
}
