#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;
	int		read;

	read = 0;
	while (s1[read])
		read++;
	if (!(s2 = malloc((sizeof(char) * (read + 1)))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
